

public class Number {

	private int num1;
	private int num2;
	private String operationPerformed;
	public Number(int num1, int num2, String operationPerformed) {
		super();
		this.num1 = num1;
		this.num2 = num2;
		this.operationPerformed = operationPerformed;
	}
	public int getNum1() {
		return num1;
	}
	public int getNum2() {
		return num2;
	}
	public String getOperationPerformed() {
		return operationPerformed;
	}
	
	
	
}
